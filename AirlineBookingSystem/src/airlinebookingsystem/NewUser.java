    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String firstname=firstnameTF.getText();
        String lastname=lastnameTF.getText();
        String username=usernameTF.getText();
        String password=passwordTF.getText();
        String email=emailTF.getText();
        String weburl=webTF.getText();
        
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
          
            String databaseURL="jdbc:mysql://localhost:3306/airline";
      
            java.sql.Connection con=DriverManager.getConnection(databaseURL,"root","");
            java.sql.Statement stat=con.createStatement();
            
            String selectQuery = "select count(*) from user_details where username='"+username+"' and password='"+password+"'";
           
            ResultSet rs=stat.executeQuery(selectQuery);
            if(!rs.next()){
                infoMessage("Already registered","Welcome!!");
            }
            else{
                String insertQuery="insert into user_details values('123','"+firstname+"','"+lastname+"','"+username+"','"+password+"','"+email+"','"+weburl+"')";
            
                stat.executeUpdate(insertQuery);
                infoMessage("Information is inserted","Welcome!!");
                dispose();
                UserLogin ln=new UserLogin();
                ln.setLocationRelativeTo(null);
                ln.setVisible(true);
            }
            
            
            
            
            
        }
        catch(Exception e)
        {  
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

