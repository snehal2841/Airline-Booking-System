    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String username=jTextField1.getText();
        String password=jPasswordField1.getText();
        try
        {
             Class.forName("com.mysql.jdbc.Driver");
          
            String databaseURL="jdbc:mysql://localhost:3306/airline";
      
            java.sql.Connection con=DriverManager.getConnection(databaseURL,"root","");
            java.sql.Statement stat=con.createStatement();
               String selectQuery = "select count(*) from user_details where username='"+username+"' and password='"+password+"'";
              ResultSet rs=stat.executeQuery(selectQuery);
              if(!rs.next())
              {
                  infoMessage("Welcome......","Alert");
                  dispose();
                  UserControlPanel ucp=new UserControlPanel(username);
                  ucp.setLocationRelativeTo(null);
                  ucp.setVisible(true);
                  
              }
              else
              {
                  infoMessage("Create New Account", "Alert");
                  dispose();
                  NewUser nu=new NewUser();
                  nu.setLocationRelativeTo(null);
                  nu.setVisible(true);
              }
        }
        catch(Exception e)
         {
                    System.out.println(e);
         }
                
    }//GEN-LAST:event_jButton1ActionPerformed

